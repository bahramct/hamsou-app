/**
 * دریافت System Prompt بر اساس نوع
 */
export function getSystemPrompt(type: keyof typeof SYSTEM_PROMPTS): string {
  return SYSTEM_PROMPTS[type];
}

/**
 * افزودن context به System Prompt
 */
export function addContextToPrompt(prompt: string, context: Record<string, any>): string {
  const contextString = Object.entries(context)
    .map(([key, value]) => `${key}: ${JSON.stringify(value)}`)
    .join('\n');
  
  return `${prompt}\n\n---\nContext:\n${contextString}`;
}

/**
 * افزودن نام کاربر به System Prompt
 */
export function addUserNameToPrompt(prompt: string, userName: string): string {
  return `${prompt}\n\n---\nکاربر: ${userName || 'کاربر'}`;
}
